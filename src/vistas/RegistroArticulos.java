    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();

        if (gestionArticulo.eliminar(codigo)) {
            JOptionPane.showMessageDialog(this, "Has Eliminado el articulo exitosamente");
            llenarTabla();
            limpiar();

        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar el articulo");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void BtnBuscarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarArticuloActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();

        if (!codigo.equals("")) {

            Articulo articulo = gestionArticulo.buscar(codigo);
            if (articulo != null) {
                txtNombre.setText(articulo.getNombre());
                txtPrecio.setText(String.valueOf(articulo.getPrecio()));
                txtCantidad.setText(String.valueOf(articulo.getCantidad()));
                txtDescripcion.setText(articulo.getDescripcion());
                txtCodigo.setEnabled(true);
                JOptionPane.showMessageDialog(this, "Articulo encontrado");
            } else {
                limpiar();
                JOptionPane.showMessageDialog(this, "No se encuentra el Articulo");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese el dato que desea buscar");
        }
    }//GEN-LAST:event_BtnBuscarArticuloActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        String descripcion = txtDescripcion.getText();

        if (gestionArticulo.modificar(codigo, nombre, precio, cantidad, descripcion)) {
            JOptionPane.showMessageDialog(this, "Modificado exitosamente");
            llenarTabla();
            limpiar();

        } else {
            JOptionPane.showMessageDialog(this, "Error al modificar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        String descripcion = txtDescripcion.getText();
        if (gestionArticulo.guardar(codigo, nombre, precio, cantidad, descripcion)) {
            JOptionPane.showMessageDialog(this, "Guardado exitosamente");
            llenarTabla();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar");
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed
    public void limpiar() {
        txtNombre.setText("");
        txtDescripcion.setText("");
        txtPrecio.setText("");
        txtCantidad.setText("");
        txtDescripcion.setText("");
        txtCodigo.setText("");
    }

    public void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        List<Articulo> lista = gestionArticulo.obtenerLista();
        model.setColumnIdentifiers(new Object[]{" codigo", "nombre", " precio", "cantidad", " descripcion",});
        TablaArticulos.setModel(model);
        for (int i = 0; i < lista.size(); i++) {
            model.addRow(new Object[]{
                lista.get(i).getCodigo(),
                lista.get(i).getNombre(),
                lista.get(i).getPrecio(),
                lista.get(i).getCantidad(),
                lista.get(i).getDescripcion(),});

        }
    }

